package com.lhfs.fsn.vo;


/**
 * 提供给监管系统VO
 * @author XinTang
 */
public class TestPropertieVO {
	private String assessment;
	private String name;
	private String result;
	private String standard;
	private String techIndicator;
	private String unit;
	public String getAssessment() {
		return assessment;
	}
	public void setAssessment(String assessment) {
		this.assessment = assessment;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getStandard() {
		return standard;
	}
	public void setStandard(String standard) {
		this.standard = standard;
	}
	public String getTechIndicator() {
		return techIndicator;
	}
	public void setTechIndicator(String techIndicator) {
		this.techIndicator = techIndicator;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	
}